package com.Stringss;

public class PreetyPrinting {
    public static void main(String[] args) {
        float num  = 123.456f;

        System.out.printf("Formatted number is %.2f",num);
        System.out.println();

        System.out.printf("Value of PI is %.8f", Math.PI);
        System.out.println();

        System.out.printf("My name is %s and my college name is %s", "Ankit", "NIET");

        /*
        There are many format specifiers we can use. Here are some common ones:
    %c - Character
    %d - Decimal number (base 10)
    %e - Exponential floating-point number
    %f - Floating-point number
    %i - Integer (base 10)
    %o - Octal number (base 8)
    %s - String
    %u - Unsigned decimal (integer) number
    %x - Hexadecimal number (base 16)
    %t - Date/time
    %n - Newline
         */
    }
}
